/**
 * The utils module gathers shared helper types for JSON parsing, hashing, and
 * packet encoding.
 * @packageDocumentation
 * @module utils
 * @since v0.0.0
 */
/// utils

/**
 * The jsonString function converts an unknown value to a normalized JSON
 * string.
 * @category Utility
 * @since v0.0.0
 */
/// jsonString

/**
 * The jsonParse function parses a JSON string that may have come from
 * jsonString.
 * @category Utility
 * @since v0.0.0
 */
/// jsonParse

/**
 * The getUniqueId function generates a unique ID string of the requested
 * length.
 * @category Utility
 * @since v0.0.0
 */
/// getUniqueId

/**
 * The getHash function produces a Hash for a given string payload.
 * @category Utility
 * @since v0.0.0
 */
/// getHash

/**
 * The isTimestamp function is a type guard that identifies Timestamp values.
 * @category Utility
 * @since v0.0.0
 */
/// isTimestamp

/**
 * The isAtom function is a type guard that identifies Atom values.
 * @category Utility
 * @since v0.0.0
 */
/// isAtom

/**
 * The getPartsFromPacket function splits a packet into the destination and body
 * parts.
 * @category Utility
 * @since v0.0.0
 */
/// getPartsFromPacket

/**
 * The getPacketFromParts function encodes a destination and body into a packet
 * string.
 * @category Utility
 * @since v0.0.0
 */
/// getPacketFromParts
